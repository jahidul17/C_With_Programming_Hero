#include<stdio.h>
int main(){
   //For Loop
    int i;
    for(i=1;i<=7;i++){

        printf("%d\n",i);
    }

    printf("\n\n");

    //While Loop
    int a=1;
    while(a<=5){
        printf("%d\n",a);
        a++;
    }

    printf("\n\n");

    //Do While Loop
    int b=15;
    do{
        printf("%d\n",b);
        b++;
    }while(b<=20);
    
       
    
    


}